#include <stdio.h>
#include<stdlib.h>

int main() {
    int integer1 = 42;
    int integer2 = 15;

    float floatVar = 5.53869;

    char charVar = 'A';

    printf("Integer 1 - %d\n", integer1);
    printf("Integer 2 - %d\n", integer2);
    printf("Float Variable - %.2f\n", floatVar);
    printf("Char Variable - %c\n", charVar);

    return 0;
}
